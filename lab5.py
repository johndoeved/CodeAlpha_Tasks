## WAP to demonstrate the indexing and slicing of a string.
str = input("Enter a string: ")
print("Original string:", str)
print("Character at index 0:", str[0]) 
print("Character at index 7:", str[7])
print("Character at index -1:", str[-1])
## Slicing
print("Substring from index 0 to 4:", str[0:5])
print("Substring from index 7 to end:", str[7:])
print("add from start to index 4:", str[:5])
